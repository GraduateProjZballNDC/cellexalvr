        public void AddVerticesToMesh()
        {
            vertices = new Vector3[n_vertices];
            if (ChunkManager.addVertexSpheres)
                spheres = new GameObject[n_vertices];
            int index = 0;
            for (int i = 0; i < ChunkManager.chunkResolution; i++)
            {
                for (int j = 0; j < ChunkManager.chunkResolution; j++)
                {
                    for (int k = 0; k < ChunkManager.chunkResolution - 1; k++)
                    {
                        bool[] cut = new bool[3];
                        float d1 = getDensity(i, j, k);
                        float d2 = getDensity(i, j, k + 1);
                        float percentage = 0.5f;
                        if (d1 < ChunkManager.surfaceLevel && ChunkManager.surfaceLevel < d2)
                        {
                            percentage = (ChunkManager.surfaceLevel - d1) / (d2 - d1);
                            cut[0] = true;
                        }
                        else if (d1 > ChunkManager.surfaceLevel && ChunkManager.surfaceLevel > d2)
                        {
                            percentage = 1 - (ChunkManager.surfaceLevel - d2) / (d1 - d2);
                            cut[0] = true;
                        }
                        vertices[index] = new Vector3(i, j, k + percentage) / (ChunkManager.chunkResolution - 1);

                        d1 = getDensity(k, i, j);
                        d2 = getDensity(k + 1, i, j);
                        percentage = 0.5f;
                        if (d1 < ChunkManager.surfaceLevel && ChunkManager.surfaceLevel < d2)
                        {
                            percentage = (ChunkManager.surfaceLevel - d1) / (d2 - d1);
                            cut[1] = true;
                        }
                        else if (d1 > ChunkManager.surfaceLevel && ChunkManager.surfaceLevel > d2)
                        {
                            percentage = 1 - (ChunkManager.surfaceLevel - d2) / (d1 - d2);
                            cut[1] = true;
                        }
                        vertices[index + 1] = new Vector3(k + percentage, i, j) / (ChunkManager.chunkResolution - 1);

                        d1 = getDensity(j, k, i);
                        d2 = getDensity(j, k + 1, i);
                        percentage = 0.5f;
                        if (d1 < ChunkManager.surfaceLevel && ChunkManager.surfaceLevel < d2)
                        {
                            percentage = (ChunkManager.surfaceLevel - d1) / (d2 - d1);
                            cut[2] = true;
                        }
                        else if (d1 > ChunkManager.surfaceLevel && ChunkManager.surfaceLevel > d2)
                        {
                            percentage = 1 - (ChunkManager.surfaceLevel - d2) / (d1 - d2);
                            cut[2] = true;
                        }
                        vertices[index + 2] = new Vector3(j, k + percentage, i) / (ChunkManager.chunkResolution - 1);

                        if (ChunkManager.addVertexSpheres)
                        {
                            for (int l = 0; l < 3; l++)
                            {
                                GameObject sphere = GameObject.CreatePrimitive(PrimitiveType.Sphere);
                                sphere.transform.SetParent(transform);
                                sphere.transform.localPosition = vertices[index + l];
                                sphere.transform.localScale = Vector3.one / ((ChunkManager.chunkResolution - 1) * 6);
                                spheres[index + l] = sphere;
                                if (cut[l])
                                    sphere.GetComponent<Renderer>().material.color = new Color(1, 0, 0, 0);
                                else
                                    sphere.GetComponent<Renderer>().material.color = new Color(0, 0, 1, 0);

                            }
                        }

                        if (i != 0 && j != 0)
                        {
                            cubes[i - 1, j - 1, k].edges[10] = index;
                            cubes[k, i - 1, j - 1].edges[5] = index + 1;
                            cubes[j - 1, k, i - 1].edges[6] = index + 2;
                        }
                        if (i != 0 && j != ChunkManager.chunkResolution - 1)
                        {
                            cubes[i - 1, j, k].edges[11] = index;
                            cubes[k, i - 1, j].edges[1] = index + 1;
                            cubes[j, k, i - 1].edges[4] = index + 2;
                        }
                        if (i != ChunkManager.chunkResolution - 1 && j != ChunkManager.chunkResolution - 1)
                        {

                            cubes[i, j, k].edges[8] = index;
                            cubes[k, i, j].edges[3] = index + 1;
                            cubes[j, k, i].edges[0] = index + 2;
                        }
                        if (i != ChunkManager.chunkResolution - 1 && j != 0)
                        {

                            cubes[i, j - 1, k].edges[9] = index;
                            cubes[k, i, j - 1].edges[7] = index + 1;
                            cubes[j - 1, k, i].edges[2] = index + 2;
                        }
                        index += 3;
                    }
                }
            }
            mesh.vertices = vertices;
        }

        public void updateVertices()
        {
            foreach (Cube c in cubes)
            {
                c.updateVertices();
            }
            mesh.vertices = vertices;
        }

        public IEnumerator UpdateVertices()
        {
            foreach (Cube c in cubes)
            {
                c.updateVertices();
                yield return null;
            }
            mesh.vertices = vertices;
        }

        public void updateTexture()
        {
            for (int i = 0; i < ChunkManager.chunkResolution; i++)
            {
                for (int j = 0; j < ChunkManager.chunkResolution; j++)
                {
                    for (int k = 0; k < ChunkManager.chunkResolution; k++)
                    {
                        tex.SetPixel(i, j, k, new Color(0, 0, 0, getDensity(i, j, k)));
                    }
                }
            }
            tex.Apply();
            material.mainTexture = tex;
        }

        public void updateTriangles()
        {
            List<int> triangles = new List<int>();
            Vector3[] normals = new Vector3[vertices.Length];
            for (int i = 0; i < ChunkManager.chunkResolution - 1; i++)
            {
                for (int j = 0; j < ChunkManager.chunkResolution - 1; j++)
                {
                    for (int k = 0; k < ChunkManager.chunkResolution - 1; k++)
                    {
                        int code = 0;
                        code += getDensity(i, j, k) < ChunkManager.surfaceLevel ? 0 : 1;
                        code += getDensity(i, j + 1, k) < ChunkManager.surfaceLevel ? 0 : 2;
                        code += getDensity(i + 1, j + 1, k) < ChunkManager.surfaceLevel ? 0 : 4;
                        code += getDensity(i + 1, j, k) < ChunkManager.surfaceLevel ? 0 : 8;
                        code += getDensity(i, j, k + 1) < ChunkManager.surfaceLevel ? 0 : 16;
                        code += getDensity(i, j + 1, k + 1) < ChunkManager.surfaceLevel ? 0 : 32;
                        code += getDensity(i + 1, j + 1, k + 1) < ChunkManager.surfaceLevel ? 0 : 64;
                        code += getDensity(i + 1, j, k + 1) < ChunkManager.surfaceLevel ? 0 : 128;
                        List<KeyValuePair<int, Vector3>> tris = new List<KeyValuePair<int, Vector3>>();
                        for (int l = 0; l < 16; l++)
                        {
                            int localVertexIndex = triangulation[code, l];
                            if (localVertexIndex == -1) break;
                            int globalVertexIndex = cubes[i, j, k].edges[localVertexIndex];
                            tris.Add(new KeyValuePair<int, Vector3>(globalVertexIndex, vertices[globalVertexIndex]));
                            triangles.Add(globalVertexIndex);
                        }
                        for (int l = 0; l < tris.Count; l += 3)
                        {
                            KeyValuePair<int, Vector3> p1 = tris[l];
                            KeyValuePair<int, Vector3> p2 = tris[l + 1];
                            KeyValuePair<int, Vector3> p3 = tris[l + 2];
                            Vector3 v1 = p2.Value - p1.Value;
                            Vector3 v2 = p3.Value - p2.Value;
                            Vector3 v3 = p1.Value - p3.Value;
                            normals[p1.Key] += Vector3.Cross(v1, v3) * Vector3.Angle(-v1, v3);
                            normals[p2.Key] += Vector3.Cross(v2, v1) * Vector3.Angle(-v2, v1);
                            normals[p3.Key] += Vector3.Cross(v3, v2) * Vector3.Angle(-v3, v2);
                        }
                    }
                }
            }
            foreach (Vector3 n in normals)
            {
                n.Normalize();
                //print(n);
            }
            mesh.normals = normals;
            mesh.triangles = triangles.ToArray();
        }

        private float getDensity(int i, int j, int k)
        {
            i += index[0] * (ChunkManager.chunkResolution - 1);
            j += index[1] * (ChunkManager.chunkResolution - 1);
            k += index[2] * (ChunkManager.chunkResolution - 1);
            return chunkManager.density[i, j, k];
        }

        private void initCubes()
        {
            for (int i = 0; i < ChunkManager.chunkResolution - 1; i++)
            {
                for (int j = 0; j < ChunkManager.chunkResolution - 1; j++)
                {
                    for (int k = 0; k < ChunkManager.chunkResolution - 1; k++)
                    {
                        cubes[i, j, k] = new Cube(i, j, k, this);
                    }
                }
            }
        }

        private class Cube
        {
            private ChunkScript c;
            public int[] edges = new int[12];
            int x, y, z;
            public Cube(int x, int y, int z, ChunkScript c)
            {
                this.x = x;
                this.y = y;
                this.z = z;
                this.c = c;
            }

            public void updateVertices()
            {
                for (int i = 0; i < 12; i++)
                {
                    float d1 = c.getDensity(x + ChunkScript.edgeConnections[i, 0, 0], y + ChunkScript.edgeConnections[i, 0, 1], z + ChunkScript.edgeConnections[i, 0, 2]);
                    float d2 = c.getDensity(x + ChunkScript.edgeConnections[i, 1, 0], y + ChunkScript.edgeConnections[i, 1, 1], z + ChunkScript.edgeConnections[i, 1, 2]);
                    float percentage = 0.5f;
                    bool cut = false;
                    if (d1 < ChunkManager.surfaceLevel && ChunkManager.surfaceLevel < d2)
                    {
                        percentage = (ChunkManager.surfaceLevel - d1) / (d2 - d1);
                        cut = true;
                    }
                    else if (d1 > ChunkManager.surfaceLevel && ChunkManager.surfaceLevel > d2)
                    {
                        percentage = 1 - (ChunkManager.surfaceLevel - d2) / (d1 - d2);
                        cut = true;
                    }
                    c.vertices[edges[i]] = Vector3.Lerp(
                        new Vector3(x + ChunkScript.edgeConnections[i, 0, 0], y + ChunkScript.edgeConnections[i, 0, 1], z + ChunkScript.edgeConnections[i, 0, 2]),
                        new Vector3(x + ChunkScript.edgeConnections[i, 1, 0], y + ChunkScript.edgeConnections[i, 1, 1], z + ChunkScript.edgeConnections[i, 1, 2]),
                        percentage
                        ) / (ChunkManager.chunkResolution - 1);
                    if (ChunkManager.addVertexSpheres)
                    {
                        c.spheres[edges[i]].transform.localPosition = c.vertices[edges[i]];
                        if (cut)
                            c.spheres[edges[i]].GetComponent<Renderer>().material.color = new Color(1, 0, 0, 0);
                        else
                            c.spheres[edges[i]].GetComponent<Renderer>().material.color = new Color(0, 0, 1, 0);
                    }

                }
            }
        }

